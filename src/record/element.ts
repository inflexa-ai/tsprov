/** Coerces a {@link DateLike} for raw time storage: strings/DateTimes kept; `Date` â†’ `DateTime`. */
function rawTime(time: DateLike): AttrValue {
  return time instanceof Date ? DateTime.fromJSDate(time) : time;
}

