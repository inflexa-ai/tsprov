/** Base class for PROV elements (nodes). Every element requires an identifier (`model.py:613`). */
export abstract class ProvElement extends ProvRecord {
  /**
   * @param bundle     The owning bundle.
   * @param identifier The element identifier â€” required.
   * @param attributes Optional initial attributes.
   * @throws {ProvElementIdentifierRequired} If `identifier` is `null` (`model.py:622`).
   */
  constructor(
    bundle: RecordBundle,
    identifier: QualifiedName | null,
    attributes?: ProvAttributes,
  ) {
    if (identifier === null) {
      throw new ProvElementIdentifierRequired();
    }
    super(bundle, identifier, attributes);
  }

  override isElement(): boolean {
    return true;
  }
}

